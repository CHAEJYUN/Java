package 연산자;

public class 연산자3 {

	public static void main(String[] args) {
		//산술연산자(+, -, *, /, %)
		int x = 99;
		int y = 10;
		
		System.out.println("더한 값 " + (x + y));
		System.out.println("뺀 값 " + (x - y));
		System.out.println("곱한 값 " + (x * y));
		System.out.println("나눈 값 " + (x / y));
		System.out.println("나머지 값 " + (x % y));
	}

}
