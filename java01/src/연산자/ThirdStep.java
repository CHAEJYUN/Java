package 연산자;

public class ThirdStep {

	public static void main(String[] args) {
		//비교연산자(==, !=, >, >=, <, <=)
		int x = 100;
		int y = 1000;
		
		System.out.println("두 수가 같은가? " + (x == y));
		System.out.println("두 수가 같지 않은가? " + (x != y));
		System.out.println("y가 x보다 큰가? " + (y > x));
		System.out.println("y가 x보다 크거나 같은가? " + (y >= x));

	}

}
